namespace Folke.Elm
{
    public class FolkeTuple
    {
    }

    public class FolkeTuple<T0>
    {
        public T0 Item0 { get; set; }
    }

    public class FolkeTuple<T0, T1>
    {
        public T0 Item0 { get; set; }
        public T1 Item1 { get; set; }
    }

    public class FolkeTuple<T0, T1, T2>
    {
        public T0 Item0 { get; set; }
        public T1 Item1 { get; set; }
        public T2 Item2 { get; set; }
    }

    public class FolkeTuple<T0, T1, T2, T3>
    {
        public T0 Item0 { get; set; }
        public T1 Item1 { get; set; }
        public T2 Item2 { get; set; }
        public T3 Item3 { get; set; }
    }

    public class FolkeTuple<T0, T1, T2, T3, T4>
    {
        public T0 Item0 { get; set; }
        public T1 Item1 { get; set; }
        public T2 Item2 { get; set; }
        public T3 Item3 { get; set; }
        public T4 Item4 { get; set; }
    }
}
